var x = 2 // declarando uma variavel chamada x e ela recebe 2.
var y = 5 // declarando uma variavel chamada y e ela recebe 5.

console.log(`O valor de x é ${x}`) // Exibe a mensagem (valor de x) no console.

console.log(`O valor de y é ${y}`) // Exibe a mensagem (valor de y) no console. 

var soma = x + y

console.log(`A soma dos valores ${x} e ${y} é ${soma}`)

soma += x // Isso e o mesmo que soma = soma + x

console.log(`O valor de soma agora é ${soma}`)

